package com.tuky.diploma.structures.addition;

public class HSLS {
    public final int y;
    public final int xL;
    public final int xR;

    public HSLS(int y, int xL, int xR) {
        this.y = y;
        this.xL = xL;
        this.xR = xR;
    }

    public int y() {
        return y;
    }

    public int xL() {
        return xL;
    }

    public int xR() {
        return xR;
    }
}
